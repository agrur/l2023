class Unary {

    public static void main(String[] args) {
        byte a = 10;
        byte b = -10;
        boolean c = true;
        boolean d = false;
        System.out.println("~a: " + ~a );
        System.out.println("~b: " + ~b);
        System.out.println("!c: " + !c);
        System.out.println("!d: " + !d);
    }

}
