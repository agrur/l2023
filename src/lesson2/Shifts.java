public class Shifts {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("10 << 2 : " + (a << 2));
        System.out.println("10 << 3 : " + (a << 3));
        System.out.println("10 << 4 : " + (a << 4) );

        System.out.println("10 >> 2 : " + (a >> 2));
        System.out.println("10 >> 3 : " + (a >> 3));
        System.out.println("10 >> 4 : " + (a >> 4) );
    }
}
